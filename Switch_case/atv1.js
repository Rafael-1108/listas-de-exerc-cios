let dia = 5;

switch (dia) {
    case 1:
        console.log("É domingo.")
break;
    case 2: 
        console.log("É segunda.")
break;
    case 3:
        console.log("É Terça.")
break;
    case 4:
        console.log("É quarta.")
break;
    case 5: 
        console.log("É quinta.")
break;
    case 6:
        console.log("É sexta.")
break;
    case 7:
        console.log("É sábado.")
break;
    default:
        console.log("Dia inválido, selecione outro entre 1 e 7.")
}

